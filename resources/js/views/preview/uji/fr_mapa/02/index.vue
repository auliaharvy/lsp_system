    objectSpanMethod1({ row, column, rowIndex, columnIndex }) {
      if(rowIndex == 0 && columnIndex == 1) {return {rowspan: 1, colspan: 2 }}
      if (columnIndex === 0) {
        if (rowIndex == 1) {
          return {
            rowspan: 2,
            colspan: 1,
          };
        } else {
          return {
            rowspan: 0,
            colspan: 0,
          };
        }
      }
    },
    objectSpanMethod2({ row, column, rowIndex, columnIndex }) {
      if (columnIndex === 0) {
        if (rowIndex === 4) {
          return {
            rowspan: 2,
            colspan: 1,
          };
        } else if (rowIndex === 5) {
          return {
            rowspan: 0,
            colspan: 0,
          };
        } else {
          return {
            rowspan: 1,
            colspan: 1,
          };
        }
      }

      return {
        rowspan: 1,
        colspan: 1,
      };
    },
  },
